/**
 * Add the new fields
 */
function add_tml_registration_form_fields() {
	tml_add_form_field( 'register', 'first_name', array(
		'type'     => 'text',
		'label'    => 'First Name',
		'value'    => tml_get_request_value( 'first_name', 'post' ),
		'id'       => 'first_name',
		'priority' => 16,
	) );
	tml_add_form_field( 'register', 'last_name', array(
		'type'     => 'text',
		'label'    => 'Last Name',
		'value'    => tml_get_request_value( 'last_name', 'post' ),
		'id'       => 'last_name',
		'priority' => 17,
	) );
  tml_add_form_field( 'register', 'country', array(
		'type' 		=> 'dropdown',
		'label' 	=> 'Country',
		'value' 	=> tml_get_request_value( 'country', 'post' ),
 		'id'       => 'country',
    'attributes' => array("style" => "font-size:130%;padding:8px;"),
		'options'	=> get_countries(),
 		'priority' => 18,
	) );
  tml_add_form_field( 'register', 'city', array(
		'type'     => 'text',
		'label'    => 'City',
		'value'    => tml_get_request_value( 'city', 'post' ),
		'id'       => 'city',
		'priority' => 19,
	) );
  tml_add_form_field( 'register', 'company', array(
		'type'     => 'text',
		'label'    => 'Company',
		'value'    => tml_get_request_value( 'company', 'post' ),
		'id'       => 'company',
		'priority' => 20,
	) );
  tml_add_form_field( 'register', 'title', array(
		'type'     => 'text',
		'label'    => 'Title',
		'value'    => tml_get_request_value( 'title', 'post' ),
		'id'       => 'title',
		'priority' => 21,
    'render_args' => array(
      'control_after' => '<p>&nbsp;</p>',
    )
	) );
}
add_action( 'init', 'add_tml_registration_form_fields' );

/**
 * Validate the new fields
 */
function validate_tml_registration_form_fields( $errors ) {
	if ( empty( $_POST['first_name'] ) ) {
		$errors->add( 'empty_first_name', '<strong>ERROR<strong>: Please enter your first name.' );
	}
	if ( empty( $_POST['last_name'] ) ) {
		$errors->add( 'empty_last_name', '<strong>ERROR</strong>: Please enter your last name.' );
	}
  if ( empty( $_POST['country'] ) ) {
		$errors->add( 'country', '<strong>ERROR</strong>: Please enter your country.' );
	}
	return $errors;
}
add_filter( 'registration_errors', 'validate_tml_registration_form_fields' );

/**
 * Save the new fields
 */
function save_tml_registration_form_fields( $user_id ) {
	if ( ! empty( $_POST['first_name'] ) ) {
		update_user_meta( $user_id, 'first_name', $_POST['first_name'] );
	}
	if ( ! empty( $_POST['last_name'] ) ) {
		update_user_meta( $user_id, 'last_name', $_POST['last_name'] );
	}
  if ( ! empty( $_POST['country'] ) ) {
		update_user_meta( $user_id, 'country', $_POST['country'] );
	}
  if ( ! empty( $_POST['city'] ) ) {
		update_user_meta( $user_id, 'city', $_POST['city'] );
	}
  if ( ! empty( $_POST['company'] ) ) {
		update_user_meta( $user_id, 'company', $_POST['company'] );
	}
  if ( ! empty( $_POST['title'] ) ) {
		update_user_meta( $user_id, 'title', $_POST['title'] );
	}
}
add_action( 'user_register', 'save_tml_registration_form_fields' );

